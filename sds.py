# Helper functions for easy access
def get_stores_for_day(day):
    """
    Get all stores scheduled for a specific day.
    
    Args:
        day (str): Day of the week (e.g., 'Monday', 'Tuesday', etc.)
    
    Returns:
        list: List of store dictionaries for that day, or empty list if day not found
    """
    return delivery_schedule.get(day, [])


def get_weekly_required_stores(day):
    """
    Get only the weekly required stores (highlighted in yellow) for a specific day.
    
    Args:
        day (str): Day of the week
    
    Returns:
        list: List of store dictionaries where weekly_required=True
    """
    stores = get_stores_for_day(day)
    return [store for store in stores if store["weekly_required"]]


def get_all_weekly_required_stores():
    """
    Get all weekly required stores across all days.
    
    Returns:
        dict: Dictionary with day as key and list of weekly required stores as value
    """
    result = {}
    for day in delivery_schedule.keys():
        result[day] = get_weekly_required_stores(day)
    return result


def search_store_by_code(code):
    """
    Search for a store across all days by its code.
    
    Args:
        code (str): Store code to search for
    
    Returns:
        dict: Dictionary with 'day' and 'store' keys if found, None otherwise
    """
    for day, stores in delivery_schedule.items():
        for store in stores:
            if store["code"] == code:
                return {"day": day, "store": store}
    return None


def search_store_by_name(name):
    """
    Search for stores across all days by name (partial match, case-insensitive).
    
    Args:
        name (str): Store name or partial name to search for
    
    Returns:
        list: List of dictionaries with 'day' and 'store' keys
    """
    results = []
    name_lower = name.lower()
    for day, stores in delivery_schedule.items():
        for store in stores:
            if name_lower in store["name"].lower():
                results.append({"day": day, "store": store})
    return results


def get_store_count_by_section():
    """
    Get count of stores for each section (day or region).
    
    Returns:
        dict: Dictionary with section name as key and store count as value
    """
    return {section: len(stores) for section, stores in delivery_schedule.items()}


